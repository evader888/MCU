/**
  * @brief This function handles DMA1 channel4 global interrupt.
  */
void DMA1_Channel4_IRQHandler(void)
{
  /* USER CODE BEGIN DMA1_Channel4_IRQn 0 */
	if(__HAL_DMA_GET_FLAG(&hdma_usart1_tx,DMA_FLAG_TC4)!=RESET)
	{
		__HAL_DMA_CLEAR_FLAG(&hdma_usart1_tx,DMA_FLAG_TC4);

		DMA_usart1_Tx_Flag = 1;		
	}
	if(__HAL_DMA_GET_FLAG(&hdma_usart1_tx,DMA_FLAG_HT4)!=RESET)
	{
		__HAL_DMA_CLEAR_FLAG(&hdma_usart1_tx,DMA_FLAG_HT4);
	}
	if(__HAL_DMA_GET_FLAG(&hdma_usart1_tx,DMA_FLAG_TE4)!=RESET)
	{
		__HAL_DMA_CLEAR_FLAG(&hdma_usart1_tx,DMA_FLAG_TE4);
	}
  /* USER CODE END DMA1_Channel4_IRQn 0 */
  HAL_DMA_IRQHandler(&hdma_usart1_tx);
  /* USER CODE BEGIN DMA1_Channel4_IRQn 1 */

  /* USER CODE END DMA1_Channel4_IRQn 1 */
}

